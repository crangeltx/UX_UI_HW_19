// .home hover & cursor change//
$(".home").mouseover(function() {
    $(this).css("color", "#00D1FF");
}).mouseleave (function() {
    $(this).css("color", "#ffffff");
});

$(".home").hover(function() {
    $(this).css("cursor","pointer");
}, function() {
    $(this).css("cursor", "auto");
});


